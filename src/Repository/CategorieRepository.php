    public function nombreArticleParCategorie()
    {
        $conn = $this->getEntityManager()->getConnection();
        $sql ='SELECT ifnull(count(a.id),0) as nombre, c.libelle as categorie
        FROM categorie c left join article a on c.id = a.categorie_id
        
        GROUP BY c.libelle
        ORDER BY c.libelle';
        $stmt = $conn->prepare($sql);
        $stmt->execute();
        return $stmt ->fetchAllAssociative();
    }

